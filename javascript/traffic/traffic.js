function stop(){
    document.getElementById("redlight").style.backgroundColor="red";
    document.getElementById("redlightbg").style.backgroundColor="red";
    document.getElementById("yellowlight").style.backgroundColor="#1f1d41";
    document.getElementById("yellowlightbg").style.backgroundColor="#4b5069";
    document.getElementById("greenlight").style.backgroundColor="#1f1d41";
    document.getElementById("greenlightbg").style.backgroundColor="#4b5069";
}
function ready(){
    document.getElementById("redlight").style.backgroundColor="#1f1d41";
    document.getElementById("redlightbg").style.backgroundColor="#4b5069";
    document.getElementById("yellowlight").style.backgroundColor="yellow";
    document.getElementById("yellowlightbg").style.backgroundColor="yellow";
    document.getElementById("greenlight").style.backgroundColor="#1f1d41";
    document.getElementById("greenlightbg").style.backgroundColor="#4b5069";
}
function go(){
    document.getElementById("redlight").style.backgroundColor="#1f1d41";
    document.getElementById("redlightbg").style.backgroundColor="#4b5069";
    document.getElementById("yellowlight").style.backgroundColor="#1f1d41";
    document.getElementById("yellowlightbg").style.backgroundColor="#4b5069";
    document.getElementById("greenlight").style.backgroundColor="green";
    document.getElementById("greenlightbg").style.backgroundColor="green";
}